#include <iostream>
#include <string>
#include <algorithm>
using namespace std;

int main() {
    string bin;
    cin >> bin;
    
    int n = 0;
    for (int i = 0; i < bin.size() - 1; i++)
        n = (n+(bin[i] ==  '1' ? 1 : 0 )) * 2;
    n += (bin[bin.size() -1 ] == '1' ? 1 : 0);
    cout << n << "\n";
    return 0;
}
